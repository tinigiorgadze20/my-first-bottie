function getRandom(someArray) {
  // рЃцрЃБрЃюрЃЦрЃўрЃфрЃљ math random [0;1) рЃерЃБрЃљрЃџрЃћрЃЊрЃерЃў рЃљрЃЉрЃарЃБрЃюрЃћрЃЉрЃА рЃарЃљрЃдрЃљрЃф рЃарЃћрЃюрЃЊрЃЮрЃЏ рЃарЃўрЃфрЃ«рЃЋрЃА.
  // рЃЏрЃўрЃќрЃљрЃюрЃў рЃўрЃДрЃЮ рЃћрЃА рЃерЃБрЃљрЃџрЃћрЃЊрЃў рЃњрЃљрЃќрЃарЃЊрЃўрЃџрЃўрЃДрЃЮ рЃЋрЃўрЃдрЃљрЃф [0;n) рЃЏрЃЊрЃћ
  //(рЃљрЃЦ n рЃљрЃарЃўрЃА arrayрЃА рЃАрЃўрЃњрЃарЃФрЃћ), рЃарЃљрЃАрЃљрЃф рЃњрЃљрЃЏрЃарЃљрЃЋрЃџрЃћрЃЉрЃўрЃА рЃЮрЃърЃћрЃарЃљрЃфрЃўрЃљ рЃљрЃЎрЃћрЃЌрЃћрЃЉрЃА рЃЊрЃљ рЃЏрЃўрЃдрЃћрЃЉрЃБрЃџрЃў double рЃбрЃўрЃърЃўрЃА (рЃљрЃюрЃБ рЃљрЃарЃљрЃЏрЃЌрЃћрЃџрЃў) рЃарЃўрЃфрЃ«рЃў рЃЏрЃЌрЃћрЃџрЃќрЃћ рЃљрЃюрЃБ integer рЃќрЃћ рЃЕрЃљрЃЏрЃЮрЃ░рЃДрЃљрЃЋрЃА.
  let x = Math.round(Math.random() * someArray.length);
  return someArray[x];
}

